package kumar;
import java.util.Scanner;
public class SwitchCase2 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
	System.out.println("Enter the number 1 to 7 :  ");
	int day = sc.nextInt();
		String result = "";
		switch(day)
		{
		case 1:
			result = "Sunday";
		 break;
		case 2:
			result = "Monday";
		break;
		case 3:
			result = "Tuesday";
		break;
		case 4:
			result = "Wednesday";
		break;
		case 5:
			result = "Thrusday";
		break;
		case 6:
			result = "Friday";
		break;
		case 7:
		  result = "Saturday";
		break;
		default:
			System.out.println("Invalid Option!!!!");
		}
		System.out.println("Day: " + result);
		}
	}

		
	
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		